
DROP TABLE Flight_Attendants;
DROP TABLE Flight_Info;
DROP TABLE Airport;
DROP TABLE Airlines; 
DROP TABLE Pilot;
DROP TABLE Travelers;


CREATE TABLE Airlines (
airline_id varchar(3) PRIMARY KEY,
airline_name varchar(255) NOT NULL UNIQUE );


CREATE TABLE Airport (
airport_id varchar(4) PRIMARY KEY, 
airport_name varchar(255) NOT NULL, 
airport_city varchar(255), 
airport_state VARCHAR(255) );

CREATE TABLE Pilot (
pilot_id int PRIMARY KEY, 
pilot_first_name varchar(255), 
pilot_last_name varchar(255),
pilot_dob date, 
pilot_hiredate date, 
pilot_salary int
);



CREATE TABLE Flight_Info (
flight_no INT PRIMARY KEY, 
flight_airline_id VARCHAR(3),
flight_dept_airport_id VARCHAR(4),
flight_arr_airport_id varchar(4), 
flight_dept_time time, 
flight_arr_time time,
flight_arr_day VARCHAR(255),
flight_gate varchar(4),
flight_pilot_id int,
FOREIGN Key (flight_airline_id) REFERENCES Airlines(airline_id),
FOREIGN KEY (flight_dept_airport_id) REFERENCES Airport(airport_id),
FOREIGN KEY (flight_arr_airport_id) REFERENCES Airport(airport_id),
FOREIGN KEY (flight_pilot_id) REFERENCES Pilot(pilot_id)
);


CREATE TABLE Travelers (
traveler_id INT PRIMARY key,
traveler_first_name varchar(255),
traveler_last_name varchar(255),
traveler_dob date, 
traveler_email varchar(255), 
traveler_phone_num varchar(50) );

CREATE TABLE Flight_Attendants (
attnd_order_id INT PRIMARY key, 
attnd_flight_no int, 
attnd_traveler_id INT, 
attnd_flight_date DATE,
FOREIGN key (attnd_flight_no) REFERENCES Flight_Info(flight_no),
FOREIGN key (attnd_traveler_id) REFERENCES Travelers(traveler_id) );

